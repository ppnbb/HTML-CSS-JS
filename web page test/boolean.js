//불리언은 2개의 데이터로 이루어져 있다. true and false.
//비교연산자 = comparison operator
console.log(1+1); //이항연산자(+) >> 왼쪽과 오른쪽의 값을 하나로 표현하는 것

console.log(1==1); //좌항과 우항 값이 같으면 true, 다르면 false.
console.log(1==2); //비교연산자 > 조건문에서 주로 사용
console.log(1>2);
console.log(1<2);
console.log(1===1);//좌항과 우항 값이 정말 같은가? 확인할 때 === 3번
console.log(1===2);
